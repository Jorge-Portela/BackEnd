package Arrays.exercise09.entities;

public class OlderPerson {
    private String name;
    private int age;

    public OlderPerson(String name, int age){
        this.name = name;
        this.age = age;
    }

    public String getName(){
        return this.name;
    }

    public int getAge(){
        return this.age;
    }

}
